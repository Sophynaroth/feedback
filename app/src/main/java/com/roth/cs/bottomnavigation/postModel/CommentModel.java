package com.roth.cs.bottomnavigation.postModel;

public class CommentModel {
    String user_profiles, user_names, comments;

    public String getUser_profiles() {
        return user_profiles;
    }

    public void setUser_profiles(String user_profiles) {
        this.user_profiles = user_profiles;
    }

    public String getUser_names() {
        return user_names;
    }

    public void setUser_names(String user_names) {
        this.user_names = user_names;
    }

    public String getComments() {
        return comments;
    }

    public void setComments(String comments) {
        this.comments = comments;
    }
}
